<script>
    function divideTeams() {
        const players = [];
        for (let i = 1; i <= 6; i++) {
            const playerName = document.getElementById(`player${i}`).value.trim();
            if (playerName) players.push(playerName);
        }

        if (players.length !== 6) {
            alert("Please enter all 6 player names.");
            return;
        }

        // Shuffle the players array
        for (let i = players.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [players[i], players[j]] = [players[j], players[i]];
        }

        // Divide players into two teams
        const team1 = players.slice(0, 3);
        const team2 = players.slice(3, 6);

        // Display teams
        document.getElementById("team1List").innerHTML = team1.map(player => `<li>${player}</li>`).join("");
        document.getElementById("team2List").innerHTML = team2.map(player => `<li>${player}</li>`).join("");
    }

    function resetTeams() {
        // Clear inputs
        for (let i = 1; i <= 6; i++) {
            document.getElementById(`player${i}`).value = "";
        }

        // Clear team lists
        document.getElementById("team1List").innerHTML = "";
        document.getElementById("team2List").innerHTML = "";
    }
</script>


   

